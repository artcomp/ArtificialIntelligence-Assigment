busca1(T) :-
   inicial1(E),
   busca1(T,[_:_:0:E:[]],[],P:G),
   tipo(T,N),
   A = P,
   while1(P),
   format('~nBusca do Tipo.: ~w~n',[N]),
   format('~nBuscando Primeiro Bloco'),
   format('~nPlano: ~w',[A]),
   format('~nCusto: ~w~n',[G]).


busca1(_,[_:_:G:E:C|_],_,P:G) :-
   meta1(E), !,
   reverse(C,P).

busca1(T,[_:_:G:E:C|F],V,P) :-
   sucessores(T,G:E:C,V,S),
   insere(T,S,F,NF),
   union([E],V,NV),
   busca1(T,NF,NV,P).


