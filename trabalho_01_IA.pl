ande(L) :- 
   pos(agente,L), !.
ande(L) :- 
   retract(pos(agente,P)), 
   assert(pos(agente,L)), 
   length(R,_), 
   rota(P,L,R), 
   siga(R), !.

pegue(O) :- 
   seg(O), !.
pegue(O) :-
   pos(O,P), 
   ande(P),         
   retract(pos(O,_)), 
   assert(seg(O)),
   get(@a,position,X),
   obj(O,No,_),
   send(No,move,X), !. 

